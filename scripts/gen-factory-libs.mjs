// Build-time generator: fetch Nord's catalog manifest and emit a committed snapshot.
// Run manually (needs network): node scripts/gen-factory-libs.mjs
// Output: src/lib/device/factory-libs.generated.ts  (do not hand-edit)
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { XMLParser } from 'fast-xml-parser';

const MANIFEST_URL = 'https://nord-sound-manager.s3.eu-west-3.amazonaws.com/clavia_sound_libraries.xml';
const OUT = fileURLToPath(new URL('../src/lib/device/factory-libs.generated.ts', import.meta.url));

const toArray = (x) => (x == null ? [] : Array.isArray(x) ? x : [x]);
const filenameFromUrl = (url) => {
  const parts = String(url).split('/').filter(Boolean);
  return parts[parts.length - 1] ?? '';
};

const xml = await (await fetch(MANIFEST_URL)).text();
const doc = new XMLParser({ ignoreAttributes: false, attributeNamePrefix: '' }).parse(xml);

const claviaVersion = String(doc?.clavia?.version ?? '');
const entries = [];
for (const lib of toArray(doc?.clavia?.sound_libraries?.lib)) {
  const type = lib.type === 'piano' ? 'piano' : 'sample';
  for (const bank of toArray(lib.bank)) {
    const category = String(bank.category ?? '');
    for (const instr of toArray(bank.instrument)) {
      for (const item of toArray(instr.item)) {
        for (const file of toArray(item.file)) {
          const url = String(file.url ?? '').trim();
          if (!url) continue;
          entries.push({
            filename: filenameFromUrl(url),
            name: String(item.name ?? instr.name ?? ''),
            version: String(item.version ?? ''),
            sizeKb: Number(file.size_kb ?? 0),
            sizeDescription: String(file.size_description ?? ''),
            category,
            type,
            url,
          });
        }
      }
    }
  }
}

if (entries.length < 50) {
  throw new Error(`manifest parse produced only ${entries.length} entries — shape may have changed; aborting`);
}

const body = `// AUTO-GENERATED by scripts/gen-factory-libs.mjs — do not hand-edit.
// Source: ${MANIFEST_URL}
// Fetched: ${new Date().toISOString().slice(0, 10)} · clavia manifest version ${claviaVersion}
export interface FactoryLibEntry {
  /** Filename from the download URL, e.g. "White_Grand_XL_6.3.npno". */
  filename: string;
  name: string;
  version: string;
  sizeKb: number;
  sizeDescription: string;
  category: string;
  type: 'piano' | 'sample';
  url: string;
}

export const FACTORY_LIBS_SOURCE = {
  url: ${JSON.stringify(MANIFEST_URL)},
  fetchedAt: ${JSON.stringify(new Date().toISOString().slice(0, 10))},
  claviaVersion: ${JSON.stringify(claviaVersion)},
};

export const FACTORY_LIBS: FactoryLibEntry[] = ${JSON.stringify(entries, null, 2)};
`;

writeFileSync(OUT, body);
console.log(`wrote ${entries.length} entries to ${OUT}`);
